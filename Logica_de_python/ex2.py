'''Desenvolva um programa que peça ao usuário dois números e exiba:

A soma

A subtração

A multiplicação

A divisão'''

numero_1 = float(input("Digite um número: "))

numero_2 = float(input("Digite outro número: "))

print(f"A soma dos números é {numero_1+numero_2}")
print(f"A subtração dos números é {numero_1-numero_2}")
print(f"A multiplicação dos números é {numero_1*numero_2}")
print(f"A divisão dos números é {numero_1/numero_2}")
